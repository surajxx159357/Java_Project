package institute_management;

public class Student{

	int id;
	String name;
	long contact;
	String email;
	double percent;
	String course;
	String stream;
	
	public Student(int id, String name, long contact, String email,double percent,String course,String stream) {
			
		this.id=id;
		this.name=name;
		this.contact=contact;
		this.email=email;
		this.course=course;
		this.stream=stream;
		this.percent=percent;
		
	}
	
	

}
